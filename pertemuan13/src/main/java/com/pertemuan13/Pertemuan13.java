public class Pertemuan13 {
    protected int tambah(int a, int b) {
        return a+b;
    }
    
    protected int kurang(int a, int b){
        return a-b;
    }
    
    protected int kali(int a, int b) {
        return a*b;
    }
    
    protected int bagi(int a, int b) {
        return a/b;
    }
}
